.global _start

_start:
/*
 * 进入 SVC 模式
 */
mrs r0, cpsr
bic r0, r0, #0x1f
orr r0, r0, #0x13   /*最低5位为 0x13 */
msr cpsr, r0

ldr sp, =0x80200000 /*设置栈指针 */
b main              /*进入main函数 */

